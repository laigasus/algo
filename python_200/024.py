# 논리 연산자 이해하기(and, or, not)
bool1 = True
bool2 = False
bool3 = True
bool4 = False
print(bool1 and bool2)  # False가 출력됨
print(bool1 and bool3)  # True가 출력됨
print(bool2 or bool3)   # True가 출력됨
print(bool2 or bool4)   # False가 출력됨
print(not bool1)        # False가 출력됨
print(not bool2)        # True가 출력됨

"""
파이썬에서
&&는 and
||는 or
!는 not
입니다
"""
